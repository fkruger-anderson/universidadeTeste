    private void iniciarBotoes() {
        btBuscar.addActionListener(e -> {
            if (getRbAlfabetica().isSelected()) {
                controller.buscarAlunosAlf();
            } else {
                controller.buscarAlunosCPF();
            }
        });
        
        cbCurso.addItemListener(e -> {
            controller.atualizarTurmas();
        });
    }

